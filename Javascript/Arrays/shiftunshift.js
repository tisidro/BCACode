let fruits = ["apple", "banana", "cherry"];
fruits.push("pineapple");
console.log(fruits) //=> ["apple", "banana", "cherry", "pineapple"])

fruits.shift()//always removes first item in array

console.log(fruits)//[ 'banana', 'cherry', 'pineapple' ]

fruits.unshift('fig');

console.log(fruits)//[ 'fig', 'banana', 'cherry', 'pineapple' ]


