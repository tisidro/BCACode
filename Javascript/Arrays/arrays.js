//here you can see how the data type Arrays works. The .length gives the length property of the array


let fruits = ["apple", "banana", "cherry"]
console.log(fruits.length) //3

console.log(fruits[fruits.length - 1])//last item in the array "Cherry"


console.log(fruits[99]) //undefined  because there's no index #99! It's saying "I don't know what the 99th item is"

console.log(fruits[0] = "melon"); //sets the zero index to "melon" (changes what is in the zero index position of the array)

